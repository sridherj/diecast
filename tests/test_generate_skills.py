def test_idempotent_second_run_produces_no_diff(tmp_path: Path) -> None:
    repo = _make_repo(tmp_path)
    _write_agent(repo, 'cast-foo')
    target = tmp_path / 'out'

    first = _run(repo, '--target-dir', str(target))
    assert first.returncode == 0
    skill = target / 'cast-foo' / 'SKILL.md'
    snapshot = skill.read_text(encoding='utf-8')

    second = _run(repo, '--target-dir', str(target))
    assert second.returncode == 0
    assert skill.read_text(encoding='utf-8') == snapshot

    # Only the cast-foo dir + (at most) backup folders should exist.
    top_level = {p.name for p in target.iterdir()}
    assert 'cast-foo' in top_level
    # Pre-existing target file from run 1 was backed up by run 2 — backup folder
    # is acceptable, but no other cast-* dirs.
    cast_dirs = {n for n in top_level if n.startswith('cast-')}
    assert cast_dirs == {'cast-foo'}


