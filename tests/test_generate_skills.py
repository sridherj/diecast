def test_single_agent_materializes_skill_md(tmp_path: Path) -> None:
    repo = _make_repo(tmp_path)
    _write_agent(repo, 'cast-foo')
    target = tmp_path / 'out'

    result = _run(repo, '--target-dir', str(target))

    assert result.returncode == 0, result.stderr
    out = target / 'cast-foo' / 'SKILL.md'
    assert out.exists()
    content = out.read_text(encoding='utf-8')
    # Generated header + back-reference
    assert 'Generated by bin/generate-skills' in content
    assert 'Source: agents/cast-foo/cast-foo.md' in content
    # Original frontmatter preserved
    assert 'name: cast-foo' in content
    assert 'description: stub' in content


def test_skill_under_claude_code_is_materialized(tmp_path: Path) -> None:
    repo = _make_repo(tmp_path)
    skill_dir = repo / 'skills' / 'claude-code' / 'cast-bar'
    skill_dir.mkdir(parents=True)
    (skill_dir / 'SKILL.md').write_text(
        '---\nname: cast-bar\ndescription: skill stub\n---\n\nhello\n',
        encoding='utf-8',
    )
    target = tmp_path / 'out'

    result = _run(repo, '--target-dir', str(target))

    assert result.returncode == 0, result.stderr
    out = target / 'cast-bar' / 'SKILL.md'
    assert out.exists()
    content = out.read_text(encoding='utf-8')
    assert 'Source: skills/claude-code/cast-bar/SKILL.md' in content


def test_non_cast_subdir_is_skipped_with_warning(tmp_path: Path) -> None:
    repo = _make_repo(tmp_path)
    _write_agent(repo, 'cast-foo')
    rogue = repo / 'agents' / 'gemini-shim'
    rogue.mkdir()
    (rogue / 'gemini-shim.md').write_text('---\nname: gemini-shim\n---\n', encoding='utf-8')
    target = tmp_path / 'out'

    result = _run(repo, '--target-dir', str(target))

    assert result.returncode == 0, result.stderr
    assert 'gemini-shim' in result.stderr
    assert 'skipped non-cast-*' in result.stderr
    assert (target / 'cast-foo' / 'SKILL.md').exists()
    assert not (target / 'gemini-shim').exists()


def test_dry_run_writes_nothing(tmp_path: Path) -> None:
    repo = _make_repo(tmp_path)
    _write_agent(repo, 'cast-foo')
    target = tmp_path / 'out'
    target.mkdir()

    result = _run(repo, '--target-dir', str(target), '--dry-run')

    assert result.returncode == 0, result.stderr
    assert 'would write' in result.stdout
    # target dir still untouched
    assert list(target.iterdir()) == []


