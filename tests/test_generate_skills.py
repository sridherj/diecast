def test_existing_target_file_is_backed_up_before_overwrite(tmp_path: Path) -> None:
    repo = _make_repo(tmp_path)
    _write_agent(repo, 'cast-foo')
    target = tmp_path / 'out'
    pre_existing = target / 'cast-foo' / 'SKILL.md'
    pre_existing.parent.mkdir(parents=True)
    pre_existing.write_text('# user manual edit\n', encoding='utf-8')

    result = _run(repo, '--target-dir', str(target))

    assert result.returncode == 0, result.stderr
    # Original file overwritten
    assert 'user manual edit' not in pre_existing.read_text(encoding='utf-8')
    # Backup folder exists with the original content preserved
    backup_dirs = sorted(target.glob('.cast-bak-*'))
    assert len(backup_dirs) == 1
    backed = backup_dirs[0] / 'cast-foo' / 'SKILL.md'
    assert backed.exists()
    assert backed.read_text(encoding='utf-8') == '# user manual edit\n'


def test_single_timestamp_per_invocation(tmp_path: Path) -> None:
    """All backups from one run land in one .cast-bak-<ts>/ folder."""
    repo = _make_repo(tmp_path)
    _write_agent(repo, 'cast-foo')
    _write_agent(repo, 'cast-bar')
    target = tmp_path / 'out'
    for name in ('cast-foo', 'cast-bar'):
        f = target / name / 'SKILL.md'
        f.parent.mkdir(parents=True)
        f.write_text(f'# old {name}\n', encoding='utf-8')

    result = _run(repo, '--target-dir', str(target))
    assert result.returncode == 0, result.stderr

    backup_dirs = sorted(target.glob('.cast-bak-*'))
    assert len(backup_dirs) == 1
    bak = backup_dirs[0]
    assert (bak / 'cast-foo' / 'SKILL.md').exists()
    assert (bak / 'cast-bar' / 'SKILL.md').exists()


def test_no_backup_when_target_is_empty(tmp_path: Path) -> None:
    repo = _make_repo(tmp_path)
    _write_agent(repo, 'cast-foo')
    target = tmp_path / 'out'

    result = _run(repo, '--target-dir', str(target))

    assert result.returncode == 0
    assert sorted(target.glob('.cast-bak-*')) == []


