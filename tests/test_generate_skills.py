"""Tests for ``bin/generate-skills``.

All tests use ``tmp_path`` and ``--target-dir`` for isolation. No test
mutates the real ``~/.claude/`` tree.

Strategy: copy ``bin/generate-skills`` into a temporary repo root that we
populate with controlled fixtures, then invoke it as a subprocess with
``--target-dir`` pointing at another tmp dir. This exercises the script
exactly as a user would run it.
"""

from __future__ import annotations

import shutil
import subprocess
import sys
from pathlib import Path

import pytest

REPO_ROOT = Path(__file__).resolve().parent.parent
SCRIPT_PATH = REPO_ROOT / 'bin' / 'generate-skills'


def _make_repo(tmp_path: Path) -> Path:
    """Create a minimal diecast-shaped repo with our script copied in."""
    repo = tmp_path / 'diecast'
    (repo / 'bin').mkdir(parents=True)
    (repo / 'agents').mkdir()
    (repo / 'skills' / 'claude-code').mkdir(parents=True)
    shutil.copy2(SCRIPT_PATH, repo / 'bin' / 'generate-skills')
    return repo


def _run(repo: Path, *args: str) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        [sys.executable, str(repo / 'bin' / 'generate-skills'), *args],
        capture_output=True,
        text=True,
        check=False,
    )


def _write_agent(repo: Path, name: str, body: str | None = None) -> Path:
    agent_dir = repo / 'agents' / name
    agent_dir.mkdir(parents=True, exist_ok=True)
    md = agent_dir / f'{name}.md'
    md.write_text(
        body
        or (
            f'---\nname: {name}\ndescription: stub\n---\n\n# {name}\n\nbody\n'
        ),
        encoding='utf-8',
    )
    return md


# --------------------------------------------------------------------------- #
# Empty-tree
# --------------------------------------------------------------------------- #


def test_help_lists_dry_run_and_target_dir(tmp_path: Path) -> None:
    repo = _make_repo(tmp_path)
    result = _run(repo, '--help')
    assert result.returncode == 0, result.stderr
    assert '--dry-run' in result.stdout
    assert '--target-dir' in result.stdout


def test_empty_tree_exits_zero_and_does_not_touch_target(tmp_path: Path) -> None:
    repo = _make_repo(tmp_path)
    target = tmp_path / 'out'
    target.mkdir()

    result = _run(repo, '--target-dir', str(target))

    assert result.returncode == 0, result.stderr
    assert 'Found 0 agents, 0 skills — nothing to generate' in result.stdout
    assert list(target.iterdir()) == []


